<?php

$x=10; 
echo ++$x; // 输出11 

$y=10; 
echo $y++; // 输出10 

$z=5; 
echo --$z; // 输出4 

$i=5; 
echo $i--; // 输出5 
$a = 10;
echo $a++;

$n1 = 10;
$n2 = 10;
$n1++;
++$n2;

echo '<br>n1:'.$n1; //输出11 结论不管放在前面还是后面都是++都为11
echo '<br>n2:'.$n2; //输出11
echo "<hr>";
$m1 = 10;
$m2 = 10;
$s1 = $m1++;
$s2 = ++$m2;
echo '<br>m1值为:'.$m1.'s1值为:'.$s1;    //m1值为:11s1值为:10  分析原理有前置操作先赋值后加
echo '<br>m2值为:'.$m2.'s2值为:'.$s2;    //m2值为:11s2值为:11
